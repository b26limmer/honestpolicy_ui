export const createStatesList = () =>
  Object.entries(states).map(([abbr, name]) => {
    return {
      value: abbr,
      text: name,
    };
  });
export const statesList = createStatesList();
export const getStateNameFromCode = code => states[code];
export const convertStateNameToCode = stateName => {
  let stateCode;
  Object.entries(states).forEach(([code, name]) => {
    if (name === stateName) {
      stateCode = code;
    }
  });
  return stateCode;
};
